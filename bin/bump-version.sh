#!/usr/bin/env bash
#
# bump-version.sh — update every version string in the repo to a new release
#
# Usage:
#   bin/bump-version.sh 0.4.4.0
#
# What it updates (in order):
#   1. CSS source file headers (tokens, core, components, utilities ×2, experimental)
#   2. cheatsheet.html (title, h1 badge, footer)
#   3. README.md (version line, PHP/WP examples)
#   4. CLAUDE.md (current version line)
#   5. docs/INSTALLATION.md (jsDelivr CDN URLs + PHP/WP examples)
#   6. docs/COMPONENTS.md (version footer line)
#   7. Regenerates css/slashed-full.css via build-bundle.sh
#
# Files intentionally NOT touched:
#   - CHANGELOG.md        (history — old versions are correct there)
#   - docs/archive/       (historical artefacts)
#   - css/slashed-full.css (regenerated by build-bundle.sh)
#
set -euo pipefail

REPO_ROOT="$(cd "$(dirname "$0")/.." && pwd)"

# ── Argument validation ───────────────────────────────────────────────────────
if [[ $# -ne 1 ]]; then
  echo "Usage: bin/bump-version.sh NEW_VERSION" >&2
  echo "  e.g. bin/bump-version.sh 0.4.4.0" >&2
  exit 1
fi

NEW="$1"

if [[ ! "$NEW" =~ ^[0-9]+\.[0-9]+\.[0-9]+(\.[0-9]+)?$ ]]; then
  echo "bump-version: version must match N.N.N or N.N.N.N, got: $NEW" >&2
  exit 1
fi

# ── Detect old version from tokens-default.css (same source as build-bundle) ──
OLD="$(grep -oE '[0-9]+\.[0-9]+\.[0-9]+(\.[0-9]+)?' \
  "$REPO_ROOT/css/tokens-default.css" | head -1)"

if [[ -z "$OLD" ]]; then
  echo "bump-version: could not detect current version from tokens-default.css" >&2
  exit 1
fi

if [[ "$OLD" == "$NEW" ]]; then
  echo "bump-version: version is already $NEW — nothing to do" >&2
  exit 0
fi

echo "bump-version: $OLD → $NEW"

# ── Helper: sed in-place, portable across macOS (BSD sed) and Linux (GNU sed) ─
sedi() {
  if sed --version 2>/dev/null | grep -q GNU; then
    sed -i "$@"
  else
    sed -i '' "$@"
  fi
}

# ── Helper: escape a string for use as a sed BRE pattern ─────────────────────
escape_sed_regex() {
  printf '%s' "$1" | sed 's/[][(){}.^$*+?|\\/]/\\&/g'
}

# ── Files to update ───────────────────────────────────────────────────────────
CSS_FILES=(
  css/tokens-default.css
  css/slashed-core.css
  css/slashed-components.css
  css/slashed-utilities.css
  css/slashed-utilities-visual.css
  css/slashed-experimental.css
)

DOC_FILES=(
  cheatsheet.html
  README.md
  CLAUDE.md
  CONTRIBUTING.md
  docs/INSTALLATION.md
  docs/COMPONENTS.md
)

old_escaped="$(escape_sed_regex "$OLD")"

for f in "${CSS_FILES[@]}" "${DOC_FILES[@]}"; do
  path="$REPO_ROOT/$f"
  if [[ ! -f "$path" ]]; then
    echo "  skip (not found): $f"
    continue
  fi
  if grep -qF "$OLD" "$path"; then
    sedi "s/${old_escaped}/${NEW}/g" "$path"
    count="$(grep -oF "$NEW" "$path" | wc -l | tr -d ' ')"
    echo "  updated ($count occurrence(s)): $f"
  else
    echo "  skip (no match):  $f"
  fi
done

# ── Regenerate bundle ─────────────────────────────────────────────────────────
echo ""
"$REPO_ROOT/bin/build-bundle.sh"

echo ""
echo "bump-version: done. Commit message suggestion:"
echo "  chore(release): bump version to $NEW"
