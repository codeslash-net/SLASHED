#!/usr/bin/env bash
#
# build-bundle.sh — regenerate css/slashed-full.css
#
# SLASHED ships hand-authored CSS with no build step for *consumers*. This
# script exists only for *maintainers*: slashed-full.css is a deterministic
# concatenation of the four core source files, and historically it was
# updated by hand. That is fragile — the bundle silently drifts when an
# edit to a source file is not mirrored. Run this script after editing any
# of the four sources, and CI verifies (.github/workflows/bundle-check.yml)
# that the bundle on disk matches what this script produces.
#
# Consumers still need no Node, no npm, no toolchain. The bundle is a
# regular committed CSS file.
#
# Usage: bin/build-bundle.sh
#
set -euo pipefail

REPO_ROOT="$(cd "$(dirname "$0")/.." && pwd)"
CSS_DIR="$REPO_ROOT/css"
OUT="$CSS_DIR/slashed-full.css"

SOURCES=(
  "tokens-default.css"
  "slashed-core.css"
  "slashed-components.css"
  "slashed-utilities.css"
)

# Pull version from the tokens-default.css banner so the bundle header
# tracks the source of truth automatically. Anchored to the specific
# banner line so a stray version string in a comment or URL elsewhere
# in the file cannot win.
VERSION="$(grep -oE 'SLASHED Tokens — Default — v[0-9]+\.[0-9]+\.[0-9]+(\.[0-9]+)?' "$CSS_DIR/tokens-default.css" | head -1 | awk '{print $NF}')"
if [[ -z "$VERSION" ]]; then
  echo "build-bundle: could not detect version from tokens-default.css banner" >&2
  exit 1
fi

TMP="$(mktemp)"
trap 'rm -f "$TMP"' EXIT

{
  printf '/* ==========================================================================\n'
  printf '   SLASHED Full Bundle — %s\n' "$VERSION"
  printf '   Concatenation of: tokens-default + slashed-core + slashed-components + slashed-utilities\n'
  printf '   Does NOT include slashed-utilities-visual.css (opt-in separately).\n'
  printf '   Does NOT include slashed-experimental.css (opt-in separately; see INSTALLATION.md).\n'
  printf '   Generated by bin/build-bundle.sh — do not edit by hand.\n'
  printf '   ========================================================================== */\n'
  printf '\n'

  for src in "${SOURCES[@]}"; do
    # awk '1; END {print ""}' prints every input line then appends one
    # extra blank line. This guarantees a clean separator regardless of
    # whether the source file ends with a trailing newline — `cat` alone
    # would glue sections together if a source ever loses its final \n.
    awk '1; END {print ""}' "$CSS_DIR/$src"
  done
} > "$TMP"

# Trim trailing blank lines, keep exactly one final newline.
# (POSIX-portable: read into a var, strip trailing whitespace, rewrite.)
content="$(cat "$TMP")"
printf '%s\n' "$content" > "$OUT"

bytes="$(wc -c < "$OUT" | tr -d ' ')"
lines="$(wc -l < "$OUT" | tr -d ' ')"
printf 'build-bundle: wrote %s (%s bytes, %s lines, version %s)\n' \
  "${OUT#$REPO_ROOT/}" "$bytes" "$lines" "$VERSION"
